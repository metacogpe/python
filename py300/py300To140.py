# for in 
과일 = ['사과','귤','수박']
for 변수 in 과일:
    print(변수)


for i in ['A','B','C']:
    print("출력:", i)


for i in ['A','B','C']:
    b = i.lower()           # 문자를 변경할 수 없어 b로 바인딩
    print("변환:", b)

data = [10,20,30]
for i in data:
    print(i)

for i in range(4): # range로 [0,1,2,3] 표현 가능
    print("-"*7)

